#La declaracion Return  se usa en la funciones y nos pemite recibir la informacion que esa funcion realzia
def multiplicar(numero1,numero2):
    multi=numero1*numero2
    return multi

#Esta es una forma de imprimir esa funcion en consola almacenando la funcion en una variable  e imprimir esa variable
resultado=multiplicar(2,4)
print(resultado)

#Esta es otra forma de imprimir esa funcion en consola
print(multiplicar(2,3))