#Funcion lower para tranformar un caracter a minuscula
letra=input("Ingrese letra:").lower()
if letra=='a':
    print("La letra es vocal")
else:
    print("La letra no es vocal")

#Funcion upper para tranformar un caracter a mayuscula
letra1=input("Ingrese letra:").upper()
if letra1=='B':
    print("La letra es vocal")
else:
    print("La letra no es vocal")