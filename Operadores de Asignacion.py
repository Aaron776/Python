#Estos operadores ayudan ahorrar codigo, para que esto de las asignaciones funciones primero se debe declarar una variable e inicializarla

#Suma en Asignacion (+=)
a=0
a+=5
print(a)

#Resta en Asignacion(-=)
b=0
b-=5
print(b)

#Multiplicacion en Asignacion(*=)
c=2
c*=5
print(c)
#Division en Asignacion(/=)
d=1
d/=5
print(d)

#Potencia en asignacion(**=)
e=2
e**=3
print(e)

#Modulo en asignacion(%=)
f=4
f%=2
print(f)