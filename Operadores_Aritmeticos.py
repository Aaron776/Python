num=1
num2=2
suma=num+num2
print("La suma es: ",suma)


#Resta
resta=num2-num
print("La resta es: ",resta)

#Multiplicacion
multiplicacion=num*num2
print("La multiplicacion es: ",multiplicacion)

#Division
#Division Normal:
division=num/num2
print("La division normal es: ",division)

#Divion Redondeada a la baja
division2=num//num2
print("La division redondeada es: ",division2)

#Modulo: el modulo es el residuo de una division
modulo= num%num2
print("El modulo es: ",modulo)

#Exponenciacion
exponenciacion=num**num2
print("La exponenciacion es: ",exponenciacion)
