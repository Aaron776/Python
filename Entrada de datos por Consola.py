#Para ingresar datos de tipo cadena por teclado se usa la opcion input
nombre=input("Digite su nombre: ")
print("Hola",nombre)

#Para ingresar datos de tipo entero(int) por teclado se usa la opcion input pero antes debes poner la palabra int
numero=int(input("Digite su numero: "))
print(numero)

#Para ingresar datos de tipo float(decimal) por teclado se usa la opcion input pero antes debes poner la palabra float
numero2=float(input("Digite su numero: "))
print(numero2)

